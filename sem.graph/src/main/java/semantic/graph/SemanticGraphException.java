package semantic.graph;


public class SemanticGraphException extends Exception {

	private static final long serialVersionUID = 1L;
	
	public SemanticGraphException() {
		super();
	}

	public SemanticGraphException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SemanticGraphException(String arg0) {
		super(arg0);
	}

	public SemanticGraphException(Throwable arg0) {
		super(arg0);
	}
}
